create or replace function public.is_nodeguard_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles
    where id = auth.uid()
      and is_active
      and role in ('admin', 'super_admin')
  );
$$;

grant execute on function public.is_nodeguard_admin() to authenticated;

