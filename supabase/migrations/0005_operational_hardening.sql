create or replace function public.protect_nodeguard_identity_columns()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if auth.role() <> 'service_role' then
    if tg_table_name = 'profiles'
      and (new.role is distinct from old.role or new.is_active is distinct from old.is_active)
    then
      raise exception 'Role and active-state changes require an administrator.';
    end if;
    if tg_table_name = 'responders'
      and not public.is_nodeguard_admin()
      and (
        new.id is distinct from old.id
        or new.profile_id is distinct from old.profile_id
        or new.public_code is distinct from old.public_code
        or new.name is distinct from old.name
        or new.role is distinct from old.role
        or new.agency_unit is distinct from old.agency_unit
        or new.contact_number is distinct from old.contact_number
        or new.current_assignment is distinct from old.current_assignment
      )
    then
      raise exception 'Responder identity and assignment changes require an administrator.';
    end if;
  end if;
  return new;
end;
$$;

