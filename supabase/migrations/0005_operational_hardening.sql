create or replace function public.assign_nodeguard_responder(
  p_responder_code text,
  p_incident_public_id text,
  p_actor_id uuid default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_actor uuid;
  v_role public.user_role;
  v_incident public.incidents%rowtype;
  v_responder public.responders%rowtype;
begin
  v_actor := case when auth.role() = 'service_role' then p_actor_id else auth.uid() end;
  select role into v_role from public.profiles where id = v_actor and is_active;
  if v_actor is null or v_role is null or (
    v_role not in ('admin', 'super_admin')
    and exists (select 1 from public.responders where profile_id = v_actor)
  ) then
    raise exception 'Authorized dispatch-desk personnel access is required.';
  end if;

  select * into v_incident
  from public.incidents
  where public_id = p_incident_public_id
  for update;
  if not found then raise exception 'Incident not found: %', p_incident_public_id; end if;
  if v_incident.status in ('resolved', 'closed', 'false_alert') then
    raise exception 'Final incidents cannot be reassigned.';
  end if;

  select * into v_responder
  from public.responders
  where public_code = p_responder_code
  for update;
  if not found then raise exception 'Responder not found: %', p_responder_code; end if;
  if v_responder.availability = 'offline' then
    raise exception 'Offline responders cannot be assigned.';
  end if;

  update public.incidents
  set assigned_responder_name = v_responder.name,
      assigned_unit = v_responder.agency_unit,
      status = 'assigned'
  where id = v_incident.id;

  update public.responders
  set availability = 'dispatched',
      current_assignment = v_incident.public_id,
      last_status_update = now()
  where id = v_responder.id;

  insert into public.incident_assignments (
    incident_id, responder_id, assigned_unit, assigned_by, notes
  ) values (
    v_incident.id, v_responder.id, v_responder.agency_unit, v_actor,
    'Assigned from NodeGuard dashboard.'
  );

  insert into public.notifications (
    recipient_profile_id, responder_id, incident_id, type, title, message
  ) values (
    v_responder.profile_id, v_responder.id, v_incident.id, 'assignment',
    'New incident assigned: ' || v_incident.public_id,
    v_incident.public_id || ' assigned to ' || v_responder.name || '.'
  );

  insert into public.audit_logs (actor_profile_id, action, entity_type, entity_id, details)
  values (
    v_actor, 'assign_responder', 'incident', v_incident.public_id,
    jsonb_build_object('responder_code', v_responder.public_code, 'responder_name', v_responder.name)
  );

  return jsonb_build_object('ok', true);
end;
$$;

grant execute on function public.assign_nodeguard_responder(text, text, uuid)
to authenticated, service_role;

create or replace function public.assign_nodeguard_resource(
  p_resource_code text,
  p_incident_public_id text,
  p_actor_id uuid default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_actor uuid;
  v_role public.user_role;
  v_incident public.incidents%rowtype;
  v_resource public.response_resources%rowtype;
begin
  v_actor := case when auth.role() = 'service_role' then p_actor_id else auth.uid() end;
  select role into v_role from public.profiles where id = v_actor and is_active;
  if v_actor is null or v_role is null or (
    v_role not in ('admin', 'super_admin')
    and exists (select 1 from public.responders where profile_id = v_actor)
  ) then
    raise exception 'Authorized dispatch-desk personnel access is required.';
  end if;

  select * into v_incident from public.incidents
  where public_id = p_incident_public_id for update;
  if not found then raise exception 'Incident not found: %', p_incident_public_id; end if;
  if v_incident.status in ('resolved', 'closed', 'false_alert') then
    raise exception 'Resources cannot be assigned to a final incident.';
  end if;

  select * into v_resource from public.response_resources
  where public_code = p_resource_code for update;
  if not found then raise exception 'Resource not found: %', p_resource_code; end if;
  if v_resource.status <> 'available' then
    raise exception 'Only available resources can be assigned.';
  end if;

  update public.response_resources
  set status = 'dispatched', assigned_incident_public_id = v_incident.public_id
  where id = v_resource.id;

  insert into public.resource_assignments (resource_id, incident_id, assigned_by, notes)
  values (v_resource.id, v_incident.id, v_actor, 'Assigned from NodeGuard dashboard.');

  insert into public.audit_logs (actor_profile_id, action, entity_type, entity_id, details)
  values (
    v_actor, 'assign_resource', 'incident', v_incident.public_id,
    jsonb_build_object('resource_code', v_resource.public_code, 'unit_name', v_resource.unit_name)
  );

  return jsonb_build_object('ok', true);
end;
$$;

