create table if not exists public.response_resources (
  id uuid primary key default gen_random_uuid(),
  public_code text not null unique,
  resource_type text not null check (resource_type in (
    'Ambulance', 'Fire Truck', 'Rescue Vehicle', 'Patrol Vehicle',
    'Communication Radio', 'First Aid Kit', 'Rescue Equipment',
    'Water Rescue Equipment'
  )),
  unit_name text not null,
  agency text not null check (agency in ('MDRRMO', 'PNP', 'BFP', 'EMS', 'Barangay Responders')),
  status text not null default 'available' check (status in (
    'available', 'dispatched', 'under_maintenance', 'unavailable', 'reserved'
  )),
  base_location text not null,
  assigned_incident_public_id text references public.incidents(public_id) on delete set null,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

