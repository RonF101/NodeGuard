create or replace function public.validate_nodeguard_incident(
  p_incident_public_id text,
  p_validation_status text,
  p_actor_id uuid default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_actor uuid;
  v_role public.user_role;
begin
  if p_validation_status not in ('pending_review', 'confirmed', 'false_alarm') then
    raise exception 'Invalid validation status.';
  end if;

  v_actor := case when auth.role() = 'service_role' then p_actor_id else auth.uid() end;
  select role into v_role from public.profiles where id = v_actor and is_active;
  if v_actor is null or v_role is null or v_role not in ('personnel', 'admin', 'super_admin') then
    raise exception 'Authorized personnel access is required.';
  end if;

  update public.incidents
  set validation_status = p_validation_status,
      validated_by = case when p_validation_status = 'pending_review' then null else v_actor end,
      validated_at = case when p_validation_status = 'pending_review' then null else now() end,
      status = case when p_validation_status = 'false_alarm' then 'false_alert' else status end
  where public_id = p_incident_public_id;
  if not found then raise exception 'Incident not found: %', p_incident_public_id; end if;

  insert into public.audit_logs (actor_profile_id, action, entity_type, entity_id, details)
  values (
    v_actor, 'validate_incident', 'incident', p_incident_public_id,
    jsonb_build_object('validation_status', p_validation_status)
  );

  return jsonb_build_object('ok', true);
end;
$$;

grant execute on function public.validate_nodeguard_incident(text, text, uuid)
to authenticated, service_role;

drop function if exists public.set_device_buzzer(text, boolean, text);
create function public.set_device_buzzer(
  p_device_id text,
  p_active boolean,
  p_source text default 'personnel_app',
  p_requested_by uuid default null
)
returns table (
  device_id text,
  buzzer_active boolean,
  buzzer_updated_at timestamptz
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_actor uuid;
  v_allowed boolean;
begin
  if p_source not in ('dashboard', 'personnel_app', 'device') then
    raise exception 'Invalid buzzer command source: %', p_source;
  end if;

  v_actor := case when auth.role() = 'service_role' then p_requested_by else auth.uid() end;
  select (
    p_source = 'device'
    or exists (
      select 1 from public.profiles p
      where p.id = v_actor and p.is_active
        and (
          p.role in ('admin', 'super_admin')
          or not exists (select 1 from public.responders own where own.profile_id = p.id)
          or exists (
            select 1
            from public.responders r
            join public.incidents i on i.assigned_responder_name = r.name
            where r.profile_id = p.id
              and i.device_id = p_device_id
              and i.status not in ('resolved', 'closed', 'false_alert')
          )
        )
    )
  ) into v_allowed;
  if not coalesce(v_allowed, false) then
    raise exception 'You are not authorized to control this node buzzer.';
  end if;

  update public.device_locations dl
  set buzzer_active = p_active, buzzer_updated_at = now()
  where dl.device_id = p_device_id
  returning dl.device_id, dl.buzzer_active, dl.buzzer_updated_at
  into device_id, buzzer_active, buzzer_updated_at;
  if not found then raise exception 'Device not found: %', p_device_id; end if;

  insert into public.device_buzzer_commands (device_id, active, requested_by, source)
  values (p_device_id, p_active, v_actor, p_source);

  insert into public.audit_logs (actor_profile_id, action, entity_type, entity_id, details)
  values (
    v_actor, 'set_device_buzzer', 'device', p_device_id,
    jsonb_build_object('active', p_active, 'source', p_source)
  );

  return next;
end;
$$;

grant execute on function public.set_device_buzzer(text, boolean, text, uuid)
to authenticated, service_role;

